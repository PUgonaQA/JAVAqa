package com.qa.day1;

public class Exercise1 {
	// Calculator Exercise
	
	public static int add(int num1, int num2) {
		return num1 + num2;	
	}
	
	public static double multiply(int num1, int num2) {
		return num1 * num2;
		}
	
	public static int subtract(int num1, int num2) {
		return num1 - num2;	
		}
	
	public static int divide(int num1, int num2) {
		return num1 / num2;	
		}
	
}
	

package com.qa.TestingLearning;

public class DataTypes {
	
	public static void Types() {
	int integer = 50;
	System.out.println("1. Integer: " + integer);
	
	long longNum = 5_555_474_343L;
	System.out.println("2. Long Number: " + longNum);
	
	float floatNum = 55.55F;
	System.out.println("3. Float Number: " + floatNum);
	
	double doubleNum = 42;
	System.out.println("4. Double Number: " + doubleNum);
	
	char singleLetter = 'B';
	System.out.println("5. Single Character: " + singleLetter);
	
	//You can define a constant value using the function "final" before the constant
	final float PI = 3.14F;
	System.out.println("6. Consant Value Pi: " + PI);
	// The constant value needs to be in ALL CAPS to initialise. 

	    }
	}


